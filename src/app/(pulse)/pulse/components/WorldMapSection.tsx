interface WorldMapSectionProps {
  className?: string;
}

export function WorldMapSection({ className }: WorldMapSectionProps) {
  const containerRef = useRef<HTMLDivElement>(null);
  const [mousePosition, setMousePosition] = useState<{ x: number; y: number } | null>(null);
  const [isHovering, setIsHovering] = useState(false);

  const handleMouseMove = (e: React.MouseEvent<HTMLDivElement>) => {
    if (!containerRef.current) return;
    const rect = containerRef.current.getBoundingClientRect();
    const x = e.clientX - rect.left;
    const y = e.clientY - rect.top;
    setMousePosition({ x, y });
  };

  const handleMouseEnter = () => {
    setIsHovering(true);
  };

  const handleMouseLeave = () => {
    setIsHovering(false);
    setMousePosition(null);
  };

  // Calculate coordinates as percentages
  const getCoordinates = () => {
    if (!mousePosition || !containerRef.current) return null;
    const rect = containerRef.current.getBoundingClientRect();
    const xPercent = ((mousePosition.x / rect.width) * 100).toFixed(1);
    const yPercent = ((mousePosition.y / rect.height) * 100).toFixed(1);
    return { x: xPercent, y: yPercent };
  };

  const coords = getCoordinates();

  return (
    <div
      ref={containerRef}
      className={`relative bg-black/50 border border-primary/20 rounded-lg p-4 md:p-6 overflow-hidden ${className}`}
      onMouseMove={handleMouseMove}
      onMouseEnter={handleMouseEnter}
      onMouseLeave={handleMouseLeave}
    >
      {/* ASCII Map */}
      <pre
        className="text-primary/60 font-mono text-[8px] md:text-[10px] leading-[1.2] select-none whitespace-pre"
        style={{
          fontFamily: "SFMono-Regular, Menlo, Monaco, Consolas, 'Liberation Mono', 'Courier New', monospace",
          filter: "drop-shadow(0 0 2px rgba(0, 255, 65, 0.3))",
        }}
      >
        {WORLD_MAP_ASCII}
      </pre>

      {/* Crosshair */}
      {isHovering && mousePosition && (
        <>
          {/* Vertical line */}
          <div
            className="absolute top-0 bottom-0 w-px bg-primary/50 pointer-events-none"
            style={{
              left: `${mousePosition.x}px`,
              zIndex: 10,
            }}
          />
          {/* Horizontal line */}
          <div
            className="absolute left-0 right-0 h-px bg-primary/50 pointer-events-none"
            style={{
              top: `${mousePosition.y}px`,
              zIndex: 10,
            }}
          />
          {/* Center point */}
          <div
            className="absolute w-2 h-2 rounded-full border border-primary bg-primary/20 pointer-events-none transform -translate-x-1/2 -translate-y-1/2"
            style={{
              left: `${mousePosition.x}px`,
              top: `${mousePosition.y}px`,
              zIndex: 11,
              boxShadow: "0 0 4px rgba(0, 255, 65, 0.6)",
            }}
          />
        </>
      )}

      {/* Coordinates Display */}
      {isHovering && coords && (
        <div
          className="absolute bg-black/90 border border-primary/40 rounded px-2 py-1 font-mono text-xs text-primary pointer-events-none z-20"
          style={{
            left: `${mousePosition!.x + 10}px`,
            top: `${mousePosition!.y - 30}px`,
            transform: mousePosition!.x > (containerRef.current?.offsetWidth || 0) - 100
              ? "translateX(-100%)"
              : "none",
            boxShadow: "0 0 8px rgba(0, 255, 65, 0.4)",
          }}
        >
          {coords.x}%, {coords.y}%
        </div>
      )}

      {/* Grid overlay for RTS game feel */}
      <div
        className="absolute inset-0 pointer-events-none opacity-10"
        style={{
          backgroundImage: `
            linear-gradient(rgba(0, 255, 65, 0.1) 1px, transparent 1px),
            linear-gradient(90deg, rgba(0, 255, 65, 0.1) 1px, transparent 1px)
          `,
          backgroundSize: "20px 20px",
        }}
      />
    </div>
  );
}

